                  esteja em fase avançada. Quando o câncer colorretal é detectado em fase assintomática, o 
                  índice de sobrevida de cinco anos alcança 90%. A grande maioria dos tumores começa como 
                  pólipos benignos e por sua ressecção uma alta porcentagem de cânceres pode ser prevenida.</p>
