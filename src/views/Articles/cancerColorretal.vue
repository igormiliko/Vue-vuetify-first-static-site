            <p>A detecção precoce de pólipos adenomatosos colorretais (precursores do CCR) e de cânceres 
                localizados é possível pela pesquisa de sangue oculto nas fezes e através de métodos
                 endoscópicos, com sigmoidoscopia flexível a cada cinco anos, a partir dos cinqüenta anos
                  (pacientes de baixo risco). Os pacientes com alto risco para desenvolvimento de câncer
                   colorretal são rastreados com colonoscopia, a partir dos quarenta anos.</p>
            <p>A colonoscopia é um exame que acessa todo o cólon, sendo considerado o exame mais 
                acurado para a pesquisa de pólipos e neoplasias colônicas. Requer um profissional 
                treinado (médico) para sua execução e exige preparo de cólon (limpeza intestinal) e 
                sedação do pacientes. A colonoscopia é útil para detecção de lesões precoces ou 
                pré-malignas, além disso possibilita, em muitos casos, a ressecção terapêutica das 
                lesões a um só tempo.</p>
            <p>Considerando os fatores de risco para se desenvolver o câncer colorretal (idade acima de 
                50 anos, dieta com alto teor de gordura, consumo elevado de carnes, baixo teor de cálcio, 
                obesidade, sedentarismo, tabagismo e pouca ingestão de fibra vegetal) torna-se indispensável
                 uma abordagem multiprofissional, com estímulo à boa alimentação e atividades físicas.</p>
            <p>Finalmente, o melhor tratamento para o câncer colorretal é o diagnóstico precoce. A melhor 
                prevenção do câncer é encontrar e remover os pólipos intestinais, além de mudanças no estilo 
                de vida.</p>
