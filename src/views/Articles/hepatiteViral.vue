            <p class="mt-12">Hepatite viral é uma inflamação do fígado causada por vírus. Os vírus que
                 mais comumente causam hepatites são os da Hepatite A (VHA), da Hepatite B (VHB) e da 
                 Hepatite C (VHC). Na maioria das vezes as hepatites virais só são diagnosticadas em 
                 exames de rotina, pois somente alguns casos evoluem com o aparecimento de sintomas 
                 inespecíficos (mal estar, náuseas, fraqueza, febre e dor no corpo) e/ou específicos 
                 (pele e olhos amarelados, urina escura, fezes esbranquiçadas, dor na região do fígado). 
                 As hepatites B e C podem se tornar crônicas podendo levar ao desenvolvimento de cirrose 
                 hepática e câncer de fígado.</p>
            <p class="text-exp-panel"><strong>Hepatite A:</strong></p>
            <p>É uma doença aguda do fígado, causada pelo vírus da hepatite A (HAV). A transmissão é 
                feita por água e alimentos contaminados (transmissão fecal-oral). Mais da metade dos 
                doentes podem ser assintomáticos. Os sintomas mais comuns são febre, dor abdominal, 
                náuseas, além de icterícia. Em 99% dos casos segue-se a recuperação e cura sem problemas.
                 Em 1% dos casos os sintomas podem ser muito mais graves e rápidos caracterizando a 
                 hepatite fulminante. Na hepatite A não há casos de doença crônica.</p>
            <p>O diagnóstico é confirmado através de exames de sangue onde são detectadas alterações de 
                enzimas hepáticas e anticorpos contra o vírus da Hepatite A (Anti HAV IgM).</p>
            <p>Não há tratamento específico. Quando necessário, usam-se medicamentos sintomáticos. Não 
                cabem restrições alimentares. Deve-se evitar o consumo de álcool e de medicações sem 
                orientação médica. Os casos de hepatite fulminante necessitam de cuidados hospitalares 
                intensivos e podem requerer transplante de fígado como única forma de tratamento.</p>
            <p>As medidas gerais para a prevenção da hepatite A são higiênicas. As vacinas com o vírus
                 inativado se mostraram seguras e eficazes, conferindo proteção de 94-100%.</p>
            
            <p class="title-exp-panel"><strong>Hepatite B:</strong></p>
            <p>A hepatite B é uma doença infecciosa causada pelo vírus da Hepatite B (HBV). As transfusões
                 de sangue são a principal via de transmissão da doença, ocorrência que se tornou rara com
                  o rígido controle dos bancos de sangue. Atualmente, o uso compartilhado de seringas e 
                  agulhas entre usuários de drogas, assim como relações sexuais sem preservativo (camisinha)
                   são as formas mais freqüentes de contaminação da população. Gestantes portadoras do vírus
                    podem transmitir a doença para os bebês, sendo o momento do parto, a oportunidade em que
                     ocorre a transmissão da doença.</p>
            <p>Muitas pessoas são assintomáticas e descobrem que são portadoras do vírus em exames de rotina. 
                Quando presentes, os sintomas ocorrem na fase aguda da doença e são semelhantes aos das 
                hepatites em geral. Após a fase aguda, que pode passar despercebida, ocorre cura em mais 
                de 95% dos adultos, porém 1 a 5% ficam com hepatite crônica. Desses, 25 a 40% podem 
                desenvolver cirrose e câncer de fígado ao longo de décadas. Em crianças o risco de tornar-se 
                portador de hepatite crônica é bem maior, cerca de 90% em recém nascidos e 50% da infância.</p>
            <p>A hepatite B aguda não requer tratamento medicamentoso específico. Medicamentos sintomáticos podem
                 ser usados ocasionalmente.</p>
            <p>O tratamento da hepatite B crônica visa prevenir a evolução para cirrose e câncer de fígado, 
                porém nem todos os portadores de hepatite B crônica precisam de tratamento imediato. Quando 
                necessário, o tratamento pode ser feito com Interferon (convencional ou peguilado) ou 
                Análogos de Nucleosídeos (lamivudina, adefovir, tenofovir e entecavir).</p>
            <p>As principais formas de prevenir o contágio da Hepatite B são: controle efetivo de bancos 
                de sangue, evitar o contato com sangue infectado, não compartilhar objetos cortantes e 
                perfurantes e usar sempre preservativo nas relações sexuais.</p>
            <p>A vacina para a hepatite B é altamente efetiva. No Brasil, a vacina está disponível 
                gratuitamente na rede pública de saúde para todas as crianças, adolescentes e adultos 
                jovens até 24 anos, trabalhadores da área da saúde, homossexuais, manicures e pedicures, 
                usuários de drogas injetáveis e outros grupos de risco.</p>

            <p class="title-exp-panel"><strong>Hepatite C:</strong></p>
            <p>A Hepatite C é a inflamação do fígado causada pelo vírus da hepatite C (VHC), transmitido
                 através do contato com sangue contaminado. Situações de risco são as transfusões de 
                 sangue (antes de 1993), a injeção compartilhada de drogas e os acidentes profissionais. 
                 A transmissão sexual do HCV não é freqüente e a transmissão da mãe para o feto é rara 
                 (cerca de 5%). Não são conhecidos casos de transmissão de hepatite C pelo leite materno. 
                 Apesar das formas conhecidas de transmissão, 20 a 30% dos casos ocorrem sem que se possa 
                 demonstrar a via de contaminação.</p>
            <p>Na maioria dos casos a hepatite C é assintomática e os portadores só percebem que estão 
                doentes anos após a infecção (vinte a trinta anos) quando apresentam um caso grave de 
                hepatite crônica com risco de cirrose e câncer no fígado. Segundo dados da Organização 
                Mundial da Saúde no Brasil teríamos de 3,5 a 4 milhões de infectados.</p>
            <p>O diagnóstico é feito através de exames de sangue que são: Anti HCV, HCV RNA qualitativo 
                e quantitativo, Genotipagem, Dosagem de enzimas Hepáticas. Deve-se ainda fazer uma biópsia
                 do fígado para avaliar o grau de acometimento do mesmo.</p>
            <p>A progressão da doença depende de vários fatores relacionados ao vírus (viremia elevada e 
                genótipo 1, por exemplo) e às condições do fígado (estado de imunodeficiência, o alcoolismo 
                crônico, co-infecção com os vírus da hepatite B e do HIV, síndrome metabólica, etc.)</p>
            <p>Ao contrário do que ocorre na hepatite B, não existe vacina para hepatite C. O tratamento está indicado principalmente
                 nos pacientes não idosos, com alterações das enzimas hepáticas e biópsia hepática 
                 demonstrando inflamação e sinais de fibrose, sugerindo risco de progressão para cirrose.
                O tratamento indicado é feito com Ribavirina e Interferon Peguilado por 24 ou 48 semanas a depender do genótipo.</p>
            <p>É considerada resposta ao tratamento da hepatite C quando o vírus continua indetectável no sangue por mais de seis 
                meses após suspensão dos medicamentos.</p>
            <p>A chance de cura da hepatite C é de 50% para o genótipo 1 e de 80% para os genótipos 2 e 3. Apesar disso, o tratamento
                 não é indicado a todos os pacientes devido ao alto índice de efeitos colaterais, alguns deles graves.</p>
            <p>Outra classe de droga que poderá ajudar no tratamento da hepatite C são os Inibidores de Proteases, que 
                bloqueiam a reprodução do vírus através de uma específica enzima viral. Eles, entretanto, não curam a 
                hepatite C, mas reduzem a carga viral à zero melhorando o efeito do tratamento padrão com Interferon e 
                Ribavirina (cura em 100% no genótipo 2 e 3 e 80% no genótipo 1).</p>
                
