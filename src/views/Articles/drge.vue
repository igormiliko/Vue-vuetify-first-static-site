        <v-container class="mt-12 pr-8 pl-8">
            <h1 class="title-blog">Manifestações extraesofágicas (ou sintomas atípicos) da Doença do Refluxo Gastroesofágico (DRGE)</h1>
            <div class="sub-title">19/04/12 Escrito por Virgínia Massote Paulinelli em Doenças</div>
            <div class="sub-title">Virgínia Massote Paulinelli</div>
            <div class="sub-title">Gastroenterologista CRM-DF: 12822</div>

            <p class="mt-12">A doença do refluxo gastroesofágico (DRGE) é muito freqüentes na
                 prática médica, e também é considerada a doença mais  comum do tubo digestivo.</p>
            <p><strong class="title-exp-panel">Definição de DRGE :</strong></p>
            <p>Doença  crônica decorrente do retorno do conteúdo do estômago  para o interior do  
            esôfago e  para os órgãos  próximos  , acarretando vários  sintomas esofágicos e  
            extraesofágicos, associados ou não a lesões da mucosa do esôfago</p>
            <p>Episódios de refluxo de curta duração , podem ocorrer em pessoas sadias  e  
                esse fenômeno denomina-se refluxo fisiológico</p>
            <p><strong class="title-exp-panel">Causa da DRGE :</strong></p>
            <p>O refluxo é causado por diversos fatores , porém existem 2 principais:</p>
            <ul>
                <li>Falência do “esfíncter inferior do esôfago” (músculo localizado na porção mais baixa de esôfago).</li>
                <li>Falência do diafragma (músculo da respiração) que se insere na transição esôfago –gástrica.</li>
            </ul>
            <p>Ambos em condições fisiológicas tem a função de prevenir o retorno  do conteúdo do estômago   para o esôfago.</p>
            <p>A DRGE se apresenta mais  frequentemente com os chamados sintomas típicos :</p>
            <ul>
                <li>Pirose(Azia);</li>
                <li>Regurgitação ( refluxo de líquidos ou alimentos do estômago à boca).</li>
            </ul>
            <p>Estima-se que 20 % dos adultos apresentem esse sintomas  pelo menos uma vez por semana e 40 % mensalmente.</p>
            <p>Existe um  subgrupo de pacientes que apresenta um conjunto de sinais  e sintomas que podem ter relação com doença 
                do refluxo , porém sem se manifestarem de forma típica ( azia e regurgitação). A esse conjunto dá-se o nome de  
                manifestações extraesofágicas  da doença do refluxo, ou sintomas atípicos.</p>
            <p>Os principais sinais e sintomas atípicos são:</p>
            <ul>
                <li><strong>Pulmonares: </strong>asma, tosse crônica, bronquite, pneumonia aspirativa, e 
                fibrose pulmonar idiopática, pneumonias de repetição, apnéia do sono, bronquiectasias, 
                doença pulmonar obstrutiva crônica.</li>
                <li><strong>Otorrinolaringológicos: </strong>
                laringite, estenose  subglótica, granuloma  de prega vocal  e carcinoma de laringe, sinusite, 
                faringite, rouquidão, pigarro, sensação de globus, otite média, úlcera de laringe.</li>
                <li><strong>Orais: </strong>
                erosão dentária, halitose( mau hálito), aftas, alterações gengivais</li>
                <li><strong>Outras: </strong>dor torácica  não cardíaca</li>
            </ul>
            <p>Portanto deve-se lembrar que na presença de tais sintomas  ,o paciente pode ser um portador
                 de Doença do Refluxo,  com suas manifestações atípicas</p>
            <p>Normalmente os médicos gastroenterologistas recebem pacientes encaminhados por pneumologistas ou Otorrinolaringologistas, 
                cujas avaliações preliminares indicavam  suspeita de DRGE.</p>

            <p><strong class="title-exp-panel">Causa das manifestações atípicas da DRGE:</strong></p>
            <p>Existem duas hipóteses principais para explicar a causa das manifestações atípicas da DRGE .</p>
            <ol>
                <li>Lesão direta na mucosa da  laringe e dos órgãos próximos,  provocada pelo  ácido vindo do estômago</li>
                <li>Refluxo na própria mucosa do esôfago distal , que estimula  o nervo vago , provocando reflexos 
                    esôfago-brônquicos aumentados  ( piora a asma , pigarro e tosse crônica por exemplo).</li>
            </ol>
            <p>Nos pacientes com alteração das contrações do esôfago ( distúrbios de motilidade) nota-se uma 
                associação importante com as manifestações atípicas respiratórias (asma, tosse crônica) , 
                podendo ser essa alteração , também considerada uma causa.</p>
            <p><strong class="title-exp-panel">Diagnóstico das manifestações atípicas</strong></p>
            <ol>
                <li><strong>Endoscopia Digestiva Alta: </strong>
                Exame que avalia o aspecto da mucosa do esôfago , estômago e duodeno. Os pacientes
                 com sintomas atípicos raramente apresentam  alteração à endoscopia: ou seja, não 
                 se visualiza esôfago machucado (Esofagites). Essa alteração é visualizada apenas 
                 em torno de 10 a 30% dos casos.</li>
  
                <li><strong>Laringoscopia: </strong>
                Exame realizado pelo otorrinolaringologista. Exame endoscópico que visualiza a laringe,
                 ideal para avaliação inicial dos pacientes com queixas otorrinolaringológicas. 
                 Inchaço da laringe é o achado mais comum.</li>
  
                <li><strong>Manometria esofágica: </strong>
                exame realizado para avaliar se as contrações do esôfago são normais ou ineficazes. 
                O achado mais comum é a motilidade esofágica ineficaz . É um exame Importante para 
                avaliação pré-operatória de pacientes que serão submetidos a tratamento cirúrgico</li>
  
                <li><strong>pHmetria esofágica de 24horas: </strong>é a  monitorização do pH esofágico 
                em 24horas, através de uma fina sonda inserida na narina . É um exame bastante sensível
                 para detectar DRGE . É considerada uma boa ferramenta para aqueles pacientes que tem 
                 Endoscopia normal.</li>
            </ol>

            <p><strong class="title-exp-panel">Tratamento das manifestações atípicas:</strong></p>
            <ol>
                <li><strong>Clínico: </strong>
                Uso de medicação que diminua a produção de ácido no estômago chamadas Inibidores de bomba de prótons
                 ( Ex:omeprazol , lanzoprazol , pantoprazol, rabeprazol e esomeprazol). Geralmente as manifestações 
                 atípicas requerem  tratamento mais prolongado e com doses superiores  , quando comparado com o 
                 tratamento da DRGE com sintomas típicos</li>
  
                <li><strong>Laringoscopia: </strong>
                Exame realizado pelo otorrinolaringologista. Exame endoscópico que visualiza a laringe,  ideal para avaliação inicial
                 dos pacientes com queixas otorrinolaringológicas . Inchaço da laringe é o achado mais comum.</li>
  
                <li><strong>Medidas comportamentais: </strong>elevação da cabeceira da cama  ( 15cm) , moderar a ingestão de alguns 
                alimentos que pioram os sintomas ( gorduras, cítricos, café, bebidas alcoólicas, bebidas gasosas, produtos à 
                base de tomate, chocolate), evitar deitar-se nas duas horas posteriores às refeições, evitar refeições  copiosas, 
                evitar o uso de roupas apertadas, suspensão do tabagismo, além de perda de peso na presença de obesidade.</li>
  
                <li><strong>Cirurgia : </strong>Atualmente é usada uma técnica cirúrgica chamada fundoplicatura  videolaparoscópica.
                 Com essa técnica constitui-se uma válvula anti-refluxo na região de falência das musculaturas citadas acima 
                 (esfíncter esofagiano e diafragma), usando uma parte do estômago denominada fundo gástrico  , para evitar que ocorra 
                 a volta do ácido do estômago para o esôfago ou faringe.</li>
            </ol>
            <p>A DRGE é uma doença de alta prevalência na população . Deve-se portanto estar sempre 
                atento à  presença das suas manifestações tanto típicas quanto atípicas. Recomenda-se
                 evitar a automedicação e procurar um médico especialista para investigar os sintomas
                  e dar início ao tratamento adequado.</p>
